// given the values for the following variables
var x = 5;
var y = 7;

// compute the sum of the two integers
var sum = x + y;

// display the values
console.log(`x = ${x}`);
console.log(`y = ${y}`);

// display the sum of the two integers to the console
console.log(`Part 1 - Compute the sum of the 2 integers`);
console.log(`${x} + ${y} = ${sum}`);

// multiply the two integers
var result = x * y;

// display the result of multiplying the two integers
console.log(`Part 2 - Multiply the 2 integers`);
console.log(`${x} * ${y} = ${result}`);

// compute the calculation using the two integers
var result = (x + y) * 3;

// display the result of tripling the sum of the two integers
console.log(`Part 3 - Multiply the sum of the 2 integers by 3`);
console.log(`(${x} + ${y}) * 3 = ${result}`);
